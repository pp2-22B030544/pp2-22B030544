#remove
thislist = ["apple", "banana", "cherry"]
thislist.remove("banana")
print(thislist)

#pop
thislist = ["apple", "banana", "cherry"]
thislist.pop(1)
print(thislist)


#pop2
thislist = ["apple", "banana", "cherry"]
thislist.pop()
print(thislist)


#del
thislist = ["apple", "banana", "cherry"]
del thislist[0]
print(thislist)

#clear
thislist = ["apple", "banana", "cherry"]
thislist.clear()
print(thislist)
